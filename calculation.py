import arcade
class button:


    def on_key_press(self, key, key_modifiers):

        if key == arcade.key.A:
            print('1')
        elif key == arcade.key.S:
            print('2')
        elif key == arcade.key.D:
            print('3')

        if key == arcade.key.J:
            print('1')
        elif key == arcade.key.K:
            print('2')
        elif key == arcade.key.L:
            print('3')

